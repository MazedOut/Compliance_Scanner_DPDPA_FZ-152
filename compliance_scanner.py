# ANSI colour codes (disabled automatically if output is not a terminal)
USE_COLOUR = sys.stdout.isatty()

def clr(text, code):
    return f"\033[{code}m{text}\033[0m" if USE_COLOUR else text

def green(t):  return clr(t, "32")
def red(t):    return clr(t, "31")
def yellow(t): return clr(t, "33")
def cyan(t):   return clr(t, "36")
def bold(t):   return clr(t, "1")
def dim(t):    return clr(t, "2")


def status_fmt(status):
    if status == CheckResult.PASS:  return green(f"{'PASS':6}")
    if status == CheckResult.FAIL:  return red(f"{'FAIL':6}")
    if status == CheckResult.WARN:  return yellow(f"{'WARN':6}")
    if status == CheckResult.SKIP:  return dim(f"{'SKIP':6}")
    return status


def conflict_status_fmt(status):
    if status == ConflictResult.ACTIVE:   return red(f"{'ACTIVE':8}")
    if status == ConflictResult.INACTIVE: return green(f"{'INACTIVE':8}")
    return status


def render_report(config, check_results, conflict_results):
    """Print the full compliance scan report to stdout."""
    W = 72
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")

    print()
    print(bold("=" * W))
    print(bold(f"  DUAL-JURISDICTION COMPLIANCE SCAN REPORT"))
    print(bold(f"  DPDPA 2023  |  Federal Law 152-FZ / 242-FZ"))
    print(bold("=" * W))
    print(f"  Target  : {bold(config['bucket_name'])}")
    print(f"  Region  : {config['region']}")
    print(f"  Origin  : {config.get('data_subject_origin','both')} data subjects")
    print(f"  PDIS    : {'Yes' if config.get('is_pdis') else 'No'}")
    print(f"  Scanned : {ts}")
    print(f"  Version : {VERSION}")
    print()

    # ── Clause-level results ──────────────────────────────────────────────
    print(bold("  CLAUSE-LEVEL RESULTS"))
    print("  " + "-" * (W - 2))
    print(f"  {'Clause ID':<28} {'Check':<36} {'Status'}")
    print("  " + "-" * (W - 2))

    for r in check_results:
        cid   = cyan(f"{r.clause_id:<28}")
        cname = f"{r.check_name:<36}"
        print(f"  {cid} {cname} {status_fmt(r.status)}")
        print(f"  {' '*28}   {dim(r.detail)}")
        if r.recommendation and r.status in (CheckResult.FAIL, CheckResult.WARN):
            # Wrap recommendation text
            words = r.recommendation.split()
            lines = []
            line  = ""
            for w in words:
                if len(line) + len(w) + 1 > 55:
                    lines.append(line)
                    line = w
                else:
                    line = (line + " " + w).strip()
            if line:
                lines.append(line)
            prefix = f"  {' '*28}   {yellow('→ ')}"
            for i, ln in enumerate(lines):
                pad = f"  {' '*28}     " if i > 0 else prefix
                print(f"{pad}{yellow(ln)}")
        print()

    # ── Conflict analysis ─────────────────────────────────────────────────
    print(bold("  CONFLICT ANALYSIS"))
    print("  " + "-" * (W - 2))

    active_count = sum(1 for c in conflict_results if c.status == ConflictResult.ACTIVE)
    if active_count == 0:
        print(f"  {green('No conflicts triggered for this configuration.')}")
    else:
        for c in conflict_results:
            print(f"  {cyan(c.conflict_id):<20} {dim(c.laws):<40} "
                  f"{conflict_status_fmt(c.status)}")
            if c.status == ConflictResult.ACTIVE:
                # Wrap detail
                words = c.detail.split()
                lines = []
                line  = ""
                for w in words:
                    if len(line) + len(w) + 1 > 62:
                        lines.append(line)
                        line = w
                    else:
                        line = (line + " " + w).strip()
                if line: lines.append(line)
                for ln in lines:
                    print(f"  {'':20}   {dim(ln)}")
                if c.recommendation:
                    words = c.recommendation.split()
                    lines = []
                    line  = ""
                    for w in words:
                        if len(line) + len(w) + 1 > 60:
                            lines.append(line)
                            line = w
                        else:
                            line = (line + " " + w).strip()
                    if line: lines.append(line)
                    for i, ln in enumerate(lines):
                        pad = f"  {'':20}   {yellow('→ ')}" if i == 0 else f"  {'':20}     "
                        print(f"{pad}{yellow(ln)}")
            print()

    # ── Summary ───────────────────────────────────────────────────────────
    total    = len(check_results)
    passed   = sum(1 for r in check_results if r.status == CheckResult.PASS)
    failed   = sum(1 for r in check_results if r.status == CheckResult.FAIL)
    warned   = sum(1 for r in check_results if r.status == CheckResult.WARN)
    skipped  = sum(1 for r in check_results if r.status == CheckResult.SKIP)
    active_c = sum(1 for c in conflict_results if c.status == ConflictResult.ACTIVE)
    total_c  = len(conflict_results)

    print(bold("  SUMMARY"))
    print("  " + "-" * (W - 2))
    print(f"  Checks passed   : {green(str(passed))} / {total}")
    print(f"  Checks failed   : {red(str(failed))} / {total}")
    print(f"  Warnings        : {yellow(str(warned))} / {total}")
    print(f"  Skipped         : {dim(str(skipped))} / {total}  (out-of-scope checks)")
    print(f"  Conflicts active: {red(str(active_c)) if active_c > 0 else green('0')} / {total_c}")

    # Overall posture
    print()
    if failed == 0 and active_c == 0:
        print(f"  {green(bold('OVERALL POSTURE: COMPLIANT'))}  — No failures or active conflicts.")
    elif failed <= 2 and active_c <= 2:
        print(f"  {yellow(bold('OVERALL POSTURE: PARTIAL'))}   — Remediation required for items above.")
    else:
        print(f"  {red(bold('OVERALL POSTURE: NON-COMPLIANT'))}  — Critical failures detected.")

    print(bold("=" * W))
    print()

    return {
        "passed": passed, "failed": failed, "warned": warned,
        "skipped": skipped, "active_conflicts": active_c,
    }


