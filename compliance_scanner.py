def run_checks(config):
    """Run all clause-level compliance checks against the config."""
    results = []
    region  = config.get("region", "unknown").lower()
    origin  = config.get("data_subject_origin", "both")
    is_pdis = config.get("is_pdis", True)

    # ── CHECK 1: 242-FZ Art.18(5) — Russian region ───────────────────────
    if origin in ("russian", "both"):
        is_russian = (
            region in RUSSIAN_REGIONS or
            any(region.startswith(p) for p in ["ru-", "russia"])
        )
        if is_russian:
            results.append(CheckResult(
                "242FZ-A18(5)",
                "Storage region (Russia primary)",
                CheckResult.PASS,
                f"Region '{region}' is a Russian-territory storage location.",
            ))
        else:
            results.append(CheckResult(
                "242FZ-A18(5)",
                "Storage region (Russia primary)",
                CheckResult.FAIL,
                f"Region '{region}' is not a Russian territory. Russian citizen data "
                f"must be initially stored in Russia (242-FZ Art.18(5)).",
                "Deploy a separate Russian-region primary bucket (Yandex Cloud "
                "ru-central1 or SberCloud). Route Russian citizen data writes there "
                "before any other processing.",
            ))
    else:
        results.append(CheckResult(
            "242FZ-A18(5)",
            "Storage region (Russia primary)",
            CheckResult.SKIP,
            "Data subject origin is Indian only — 242-FZ localisation does not apply.",
        ))

    # ── CHECK 2: DPDP-S16 — DPDPA blacklist ──────────────────────────────
    region_blacklisted = region in DPDPA_BLACKLISTED_REGIONS
    if region_blacklisted:
        results.append(CheckResult(
            "DPDP-S16",
            "Storage region (DPDPA blacklist)",
            CheckResult.FAIL,
            f"Region '{region}' is in a country notified as restricted under DPDPA S.16.",
            "Migrate data to a non-blacklisted region. Review Central Government "
            "notifications under DPDPA S.16 for the current restricted list.",
        ))
    else:
        results.append(CheckResult(
            "DPDP-S16",
            "Storage region (DPDPA blacklist)",
            CheckResult.PASS,
            f"Region '{region}' is not in any country currently blacklisted under "
            f"DPDPA S.16. No Central Government restriction notification is active.",
        ))

    # ── CHECK 3: DPDP-S8(5) — Encryption at rest ─────────────────────────
    enc_enabled = config.get("encryption_enabled", False)
    enc_algo    = config.get("encryption_algorithm") or ""
    if enc_enabled:
        algo_upper = enc_algo.upper()
        is_dpdpa_ok = any(a.upper() in algo_upper for a in DPDPA_ACCEPTABLE_ALGORITHMS) or len(algo_upper) > 0
        if is_dpdpa_ok:
            results.append(CheckResult(
                "DPDP-S8(5)",
                "Encryption at rest",
                CheckResult.PASS,
                f"Encryption is enabled using '{enc_algo}'. This satisfies DPDPA S.8(5) "
                f"reasonable security safeguards standard.",
            ))
        else:
            results.append(CheckResult(
                "DPDP-S8(5)",
                "Encryption at rest",
                CheckResult.WARN,
                f"Encryption is enabled but algorithm '{enc_algo}' could not be verified "
                f"against known acceptable standards.",
                "Confirm the encryption algorithm meets industry standards (AES-256 minimum).",
            ))
    else:
        results.append(CheckResult(
            "DPDP-S8(5)",
            "Encryption at rest",
            CheckResult.FAIL,
            "Encryption at rest is NOT enabled. This violates DPDPA S.8(5) reasonable "
            "security safeguards obligation. Penalty up to INR 250 crore.",
            "Enable server-side encryption immediately. Use AES-256 at minimum.",
        ))

    # ── CHECK 4: 152FZ-A19(3b) — GOST encryption standard ───────────────
    if is_pdis and origin in ("russian", "both"):
        algo_upper = enc_algo.upper() if enc_algo else ""
        is_gost = any(g.upper() in algo_upper for g in FZ_ACCEPTABLE_ALGORITHMS)
        is_aes  = "AES" in algo_upper or "KMS" in algo_upper

        if is_gost:
            results.append(CheckResult(
                "152FZ-A19(3b)",
                "Encryption standard (GOST)",
                CheckResult.PASS,
                f"FSB-certified GOST algorithm detected ('{enc_algo}'). Satisfies "
                f"152-FZ Art.19(3b) mandatory certified tool requirement for PDIS.",
            ))
        elif is_aes and enc_enabled:
            results.append(CheckResult(
                "152FZ-A19(3b)",
                "Encryption standard (GOST)",
                CheckResult.WARN,
                f"Standard algorithm detected ('{enc_algo}'). AES satisfies DPDPA but "
                f"does NOT satisfy 152-FZ Art.19(3b) for regulated PDIS — FSB-certified "
                f"GOST tools are mandatory.",
                "Implement CONFLICT-03 hybrid by data type: use FSB-certified GOST tools "
                "(CryptoPro CSP or ViPNet) for Russian PDIS data. AES remains for "
                "non-Russian data. Separate HSM and PKI required.",
            ))
        else:
            results.append(CheckResult(
                "152FZ-A19(3b)",
                "Encryption standard (GOST)",
                CheckResult.FAIL,
                "No encryption detected. 152-FZ Art.19(3b) requires FSB-certified GOST "
                "cryptographic tools in all regulated PDIS.",
                "Deploy FSB-certified GOST encryption (CryptoPro or ViPNet) immediately. "
                "This is a verifiable technical violation auditable by FSTEC.",
            ))
    else:
        results.append(CheckResult(
            "152FZ-A19(3b)",
            "Encryption standard (GOST)",
            CheckResult.SKIP,
            "Not a regulated PDIS or no Russian data subjects — 152-FZ Art.19(3b) "
            "GOST certification check not applicable.",
        ))

    # ── CHECK 5: DPDP-S8(5) — Public access blocked ──────────────────────
    public_blocked = config.get("public_access_blocked", False)
    if public_blocked:
        results.append(CheckResult(
            "DPDP-S8(5)",
            "Public access blocked",
            CheckResult.PASS,
            "Public access is fully disabled. Satisfies DPDPA S.8(5) reasonable "
            "security safeguards and 152-FZ Art.7 non-disclosure obligations.",
        ))
    else:
        results.append(CheckResult(
            "DPDP-S8(5)",
            "Public access blocked",
            CheckResult.FAIL,
            "Public access is NOT fully blocked. Personal data in this storage "
            "may be accessible to unauthorised parties.",
            "Enable all four public access block settings: BlockPublicAcls, "
            "IgnorePublicAcls, BlockPublicPolicy, RestrictPublicBuckets.",
        ))

    # ── CHECK 6: 152FZ-A19 / DPDP-S8(5) — Versioning ────────────────────
    versioning = config.get("versioning_enabled", False)
    if versioning:
        results.append(CheckResult(
            "152FZ-A19(8)",
            "Versioning enabled (audit trail)",
            CheckResult.PASS,
            "Object versioning is enabled. Supports immutable audit trail requirement "
            "under 152-FZ Art.19(8) and DPDPA S.6(10) consent log preservation.",
        ))
    else:
        results.append(CheckResult(
            "152FZ-A19(8)",
            "Versioning enabled (audit trail)",
            CheckResult.FAIL,
            "Object versioning is NOT enabled. 152-FZ Art.19(8) requires all actions "
            "involving personal data to be registered and recorded. Without versioning, "
            "records can be silently overwritten or deleted.",
            "Enable bucket versioning. Configure lifecycle rules to transition old "
            "versions to cheaper storage rather than immediate deletion.",
        ))

    # ── CHECK 7: 152FZ-A19 / DPDP-S8(5) — Access logging ────────────────
    logging_enabled = config.get("logging_enabled", False)
    if logging_enabled:
        results.append(CheckResult(
            "DPDP-S8(5) / 152FZ-A19",
            "Access logging enabled",
            CheckResult.PASS,
            "Access logging is configured. Satisfies DPDPA S.8(5) security safeguards "
            "and 152-FZ Art.19(8) requirement to register all actions involving "
            "personal data in PDIS.",
        ))
    else:
        results.append(CheckResult(
            "DPDP-S8(5) / 152FZ-A19",
            "Access logging enabled",
            CheckResult.FAIL,
            "Access logging is NOT configured. Both DPDPA S.8(5) and 152-FZ Art.19(8) "
            "require access logs for personal data storage. Without logging, breach "
            "detection and regulatory evidence production are impossible.",
            "Enable server access logging to a separate dedicated log bucket. "
            "Set log retention to at least 1 year. Enable SIEM integration.",
        ))

    # ── CHECK 8: DPDP-S8(7) / 152FZ-A5(7) — Retention policy ────────────
    retention = config.get("retention_policy_set", False)
    if retention:
        results.append(CheckResult(
            "DPDP-S8(7) / 152FZ-A5(7)",
            "Retention / deletion policy",
            CheckResult.PASS,
            "A lifecycle / retention policy is configured. Supports DPDPA S.8(7) "
            "erasure obligation and 152-FZ Art.5(7) storage limitation principle. "
            "Verify policy enforces deletion when purpose is achieved.",
        ))
    else:
        results.append(CheckResult(
            "DPDP-S8(7) / 152FZ-A5(7)",
            "Retention / deletion policy",
            CheckResult.FAIL,
            "No retention or lifecycle deletion policy found. DPDPA S.8(7) requires "
            "erasure when the processing purpose is no longer served. 152-FZ Art.5(7) "
            "requires destruction once processing purpose is attained.",
            "Create a lifecycle policy with expiry rules mapped to each data type's "
            "retention period. Implement legal hold flags for data subject to sector-law "
            "retention overrides (e.g., banking: 10 years under RBI KYC Directions).",
        ))

    # ── CHECK 9: DPDP-S8(5) / 152FZ-A19 — HTTPS only ────────────────────
    https_only = config.get("https_only_enforced", False)
    if https_only:
        results.append(CheckResult(
            "DPDP-S8(5) / 152FZ-A19",
            "HTTPS-only (transfer encryption)",
            CheckResult.PASS,
            "HTTPS-only policy is enforced. HTTP access is blocked. Satisfies DPDPA "
            "S.8(5) transport security and 152-FZ Art.19 data transfer protection.",
        ))
    else:
        results.append(CheckResult(
            "DPDP-S8(5) / 152FZ-A19",
            "HTTPS-only (transfer encryption)",
            CheckResult.FAIL,
            "HTTPS-only is NOT enforced. HTTP (unencrypted) access to personal data "
            "is possible. This violates DPDPA S.8(5) and 152-FZ Art.19 simultaneously.",
            "Add a bucket policy denying s3:* where aws:SecureTransport is false. "
            "For 152-FZ PDIS: this endpoint must also support GOST-TLS cipher suites.",
        ))

    return results


