def fetch_aws_config(bucket_name, region=None):
    """
    Fetch real S3 bucket configuration using boto3.
    Returns a config dict in the same format as simulated configs.
    """
    try:
        import boto3
        from botocore.exceptions import ClientError, NoCredentialsError
    except ImportError:
        print("\n  ERROR: boto3 is not installed.")
        print("  Run: pip install boto3")
        print("  Then configure AWS credentials:")
        print("    aws configure")
        print("  Or set environment variables:")
        print("    export AWS_ACCESS_KEY_ID=your_key")
        print("    export AWS_SECRET_ACCESS_KEY=your_secret")
        print("    export AWS_DEFAULT_REGION=us-east-1\n")
        sys.exit(1)

    try:
        client = boto3.client("s3", region_name=region)
        print(f"\n  Connecting to AWS S3...")
        print(f"  Fetching configuration for bucket: {bucket_name}")

        config = {
            "bucket_name":          bucket_name,
            "region":               "unknown",
            "encryption_enabled":   False,
            "encryption_algorithm": None,
            "public_access_blocked": False,
            "versioning_enabled":   False,
            "logging_enabled":      False,
            "retention_policy_set": False,
            "https_only_enforced":  False,
            "data_subject_origin":  "both",  # Conservative: assume both
            "is_pdis":              True,     # Conservative: assume PDIS
        }

        # ── Region ─────────────────────────────────────────────────────
        try:
            loc = client.get_bucket_location(Bucket=bucket_name)
            raw_region = loc.get("LocationConstraint") or "us-east-1"
            config["region"] = raw_region
            print(f"  [1/7] Region: {raw_region}")
        except ClientError as e:
            print(f"  [1/7] Region: FETCH ERROR — {e.response['Error']['Code']}")

        # ── Encryption ─────────────────────────────────────────────────
        try:
            enc = client.get_bucket_encryption(Bucket=bucket_name)
            rules = enc["ServerSideEncryptionConfiguration"]["Rules"]
            if rules:
                algo = rules[0]["ApplyServerSideEncryptionByDefault"]["SSEAlgorithm"]
                config["encryption_enabled"]   = True
                config["encryption_algorithm"] = algo
                print(f"  [2/7] Encryption: ENABLED ({algo})")
            else:
                print(f"  [2/7] Encryption: DISABLED (no rules)")
        except ClientError as e:
            code = e.response["Error"]["Code"]
            if code == "ServerSideEncryptionConfigurationNotFoundError":
                print(f"  [2/7] Encryption: DISABLED (not configured)")
            else:
                print(f"  [2/7] Encryption: FETCH ERROR — {code}")

        # ── Public Access Block ────────────────────────────────────────
        try:
            pub = client.get_public_access_block(Bucket=bucket_name)
            cfg = pub["PublicAccessBlockConfiguration"]
            all_blocked = all([
                cfg.get("BlockPublicAcls", False),
                cfg.get("IgnorePublicAcls", False),
                cfg.get("BlockPublicPolicy", False),
                cfg.get("RestrictPublicBuckets", False),
            ])
            config["public_access_blocked"] = all_blocked
            status = "ALL BLOCKED" if all_blocked else "PARTIALLY OPEN"
            print(f"  [3/7] Public access: {status}")
        except ClientError as e:
            code = e.response["Error"]["Code"]
            if code == "NoSuchPublicAccessBlockConfiguration":
                print(f"  [3/7] Public access: NOT CONFIGURED (open by default)")
            else:
                print(f"  [3/7] Public access: FETCH ERROR — {code}")

        # ── Versioning ────────────────────────────────────────────────
        try:
            ver = client.get_bucket_versioning(Bucket=bucket_name)
            enabled = ver.get("Status") == "Enabled"
            config["versioning_enabled"] = enabled
            print(f"  [4/7] Versioning: {'ENABLED' if enabled else 'DISABLED'}")
        except ClientError as e:
            print(f"  [4/7] Versioning: FETCH ERROR — {e.response['Error']['Code']}")

        # ── Logging ───────────────────────────────────────────────────
        try:
            log = client.get_bucket_logging(Bucket=bucket_name)
            enabled = "LoggingEnabled" in log
            config["logging_enabled"] = enabled
            if enabled:
                target = log["LoggingEnabled"].get("TargetBucket", "same bucket")
                print(f"  [5/7] Logging: ENABLED (→ {target})")
            else:
                print(f"  [5/7] Logging: DISABLED")
        except ClientError as e:
            print(f"  [5/7] Logging: FETCH ERROR — {e.response['Error']['Code']}")

        # ── Lifecycle / Retention ─────────────────────────────────────
        try:
            lc = client.get_bucket_lifecycle_configuration(Bucket=bucket_name)
            rules = lc.get("Rules", [])
            has_expiry = any(
                "Expiration" in r or "NoncurrentVersionExpiration" in r
                for r in rules
            )
            config["retention_policy_set"] = has_expiry
            print(f"  [6/7] Lifecycle policy: {'SET' if has_expiry else 'NO EXPIRY RULES'} ({len(rules)} rules)")
        except ClientError as e:
            code = e.response["Error"]["Code"]
            if code in ("NoSuchLifecycleConfiguration", "NoSuchBucketPolicy"):
                print(f"  [6/7] Lifecycle policy: NOT CONFIGURED")
            else:
                print(f"  [6/7] Lifecycle policy: FETCH ERROR — {code}")

        # ── HTTPS-only bucket policy ──────────────────────────────────
        try:
            pol = client.get_bucket_policy(Bucket=bucket_name)
            policy_doc = json.loads(pol["Policy"])
            https_only = False
            for stmt in policy_doc.get("Statement", []):
                cond = stmt.get("Condition", {})
                if "Bool" in cond:
                    val = cond["Bool"].get("aws:SecureTransport")
                    if val in ("false", False) and stmt.get("Effect") == "Deny":
                        https_only = True
                        break
            config["https_only_enforced"] = https_only
            print(f"  [7/7] HTTPS-only: {'ENFORCED' if https_only else 'NOT ENFORCED'}")
        except ClientError as e:
            code = e.response["Error"]["Code"]
            if code == "NoSuchBucketPolicy":
                print(f"  [7/7] HTTPS-only: NO BUCKET POLICY (not enforced)")
            else:
                print(f"  [7/7] HTTPS-only: FETCH ERROR — {code}")

        print()
        return config

    except Exception as e:
        print(f"\n  FATAL: Could not connect to AWS — {e}")
        print("  Check your AWS credentials and bucket name.\n")
        sys.exit(1)


