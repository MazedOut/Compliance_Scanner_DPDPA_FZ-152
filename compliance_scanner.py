# CONFLICT ANALYSIS
# ═══════════════════════════════════════════════════════════════════════════

def analyse_conflicts(config, check_results):
    """
    Evaluate which of the 6 identified conflicts are ACTIVE based on
    the check results for this specific configuration.
    """
    conflicts = []
    region    = config.get("region", "unknown").lower()
    enc_algo  = (config.get("encryption_algorithm") or "").upper()
    origin    = config.get("data_subject_origin", "both")
    is_pdis   = config.get("is_pdis", True)

    # Build a quick lookup of check statuses
    status_by_clause = {}
    for r in check_results:
        status_by_clause[r.clause_id] = r.status

    # ── CONFLICT-01: Consent architecture ────────────────────────────────
    # Storage-level: not directly detectable from bucket config alone.
    # But we can flag it as relevant if the system serves both jurisdictions.
    if origin == "both":
        conflicts.append(ConflictResult(
            "CONFLICT-01",
            "DPDP S.6(1) vs 152-FZ Art.9(4)",
            ConflictResult.ACTIVE,
            "This storage serves both Indian and Russian data subjects. Consent "
            "collection architecture requires dual-tier implementation: DPDPA accepts "
            "UI-based affirmative action; 152-FZ Art.9(4) requires a written document "
            "with 9 mandatory elements including passport number and qualified e-signature.",
            "Implement jurisdiction-aware consent engine. Route Russian users to "
            "9-element written consent form; Indian users to UI-based granular consent. "
            "Shared immutable consent audit log with jurisdiction flags.",
        ))
    else:
        conflicts.append(ConflictResult(
            "CONFLICT-01",
            "DPDP S.6(1) vs 152-FZ Art.9(4)",
            ConflictResult.INACTIVE,
            f"Single-jurisdiction data origin ('{origin}'). Consent architecture "
            f"conflict not triggered for this configuration.",
            None,
        ))

    # ── CONFLICT-02: Data localization ────────────────────────────────────
    is_russian_region = (
        region in RUSSIAN_REGIONS or
        any(region.startswith(p) for p in ["ru-", "russia"])
    )
    is_dpdpa_blocked = region in DPDPA_BLACKLISTED_REGIONS

    if origin in ("russian", "both") and not is_russian_region:
        conflicts.append(ConflictResult(
            "CONFLICT-02",
            "242FZ-A18(5) vs DPDP-S16",
            ConflictResult.ACTIVE,
            f"Region '{region}' satisfies DPDPA S.16 (not blacklisted) but violates "
            f"242-FZ Art.18(5) (not a Russian territory). Russian citizen data must be "
            f"initially stored in Russia. DPDPA requires no localization — the two laws "
            f"impose opposite architectural defaults.",
            "Deploy a separate Russian-territory primary bucket (Yandex Cloud "
            "ru-central1 or SberCloud). Implement jurisdictional data routing at "
            "application layer: Russian citizen writes → Russian primary; all others → "
            "current bucket. The bifurcation is architecturally mandatory.",
        ))
    elif is_dpdpa_blocked and is_russian_region:
        conflicts.append(ConflictResult(
            "CONFLICT-02",
            "242FZ-A18(5) vs DPDP-S16",
            ConflictResult.ACTIVE,
            f"Region '{region}' satisfies 242-FZ (Russian territory) but is in a "
            f"country currently blacklisted under DPDPA S.16. Cannot simultaneously "
            f"satisfy both laws with a single storage location.",
            "Contact legal counsel. This configuration represents a currently "
            "irreconcilable conflict requiring regulatory guidance.",
        ))
    else:
        conflicts.append(ConflictResult(
            "CONFLICT-02",
            "242FZ-A18(5) vs DPDP-S16",
            ConflictResult.INACTIVE,
            f"Region '{region}' satisfies both laws currently: "
            f"{'Russian territory (242-FZ ok)' if is_russian_region else 'Not blacklisted (DPDPA ok)'}. "
            f"Monitor DPDPA S.16 blacklist notifications.",
            None,
        ))

    # ── CONFLICT-03: Cryptographic standards ──────────────────────────────
    is_gost = any(g.upper() in enc_algo for g in FZ_ACCEPTABLE_ALGORITHMS)
    is_aes  = ("AES" in enc_algo or "KMS" in enc_algo) and not is_gost
    enc_on  = config.get("encryption_enabled", False)

    if is_pdis and origin in ("russian", "both") and enc_on and is_aes:
        conflicts.append(ConflictResult(
            "CONFLICT-03",
            "152FZ-A19(3b) vs DPDP-S8(5)",
            ConflictResult.ACTIVE,
            f"Encryption algorithm '{config.get('encryption_algorithm')}' satisfies "
            f"DPDPA S.8(5) reasonable safeguards standard (AES accepted) but does NOT "
            f"satisfy 152-FZ Art.19(3b) for regulated PDIS (FSB-certified GOST tools "
            f"mandatory). GOST and AES are incompatible cipher suites.",
            "Implement hybrid encryption by data type: FSB-certified GOST tools "
            "(CryptoPro CSP or ViPNet) for Russian PDIS data; AES-256 for all other "
            "data. Implement crypto abstraction layer — algorithm selection as "
            "deployment-time config. Separate HSMs and PKIs required per jurisdiction.",
        ))
    elif not enc_on:
        conflicts.append(ConflictResult(
            "CONFLICT-03",
            "152FZ-A19(3b) vs DPDP-S8(5)",
            ConflictResult.ACTIVE,
            "No encryption is enabled. Both laws require encryption; 152-FZ additionally "
            "mandates FSB-certified GOST tools. Both violations are simultaneously active.",
            "Enable encryption immediately. Use GOST for Russian PDIS data, AES-256 "
            "elsewhere. See CONFLICT-03 resolution in research paper Section 4.",
        ))
    else:
        note = "GOST detected — satisfies 152-FZ" if is_gost else "Single-jurisdiction or non-PDIS — GOST not required"
        conflicts.append(ConflictResult(
            "CONFLICT-03",
            "152FZ-A19(3b) vs DPDP-S8(5)",
            ConflictResult.INACTIVE,
            f"Cryptographic conflict not triggered for this configuration. {note}.",
            None,
        ))

    # ── CONFLICT-04: Security control prescription ────────────────────────
    # Detectable symptom: no logging + no versioning = likely no 152-FZ UZ compliance
    logging_ok    = config.get("logging_enabled", False)
    versioning_ok = config.get("versioning_enabled", False)
    if is_pdis and origin in ("russian", "both") and (not logging_ok or not versioning_ok):
        missing = []
        if not logging_ok:    missing.append("access logging")
        if not versioning_ok: missing.append("versioning")
        conflicts.append(ConflictResult(
            "CONFLICT-04",
            "DPDP-S8(4) vs 152FZ-A19(1)/A19(3)",
            ConflictResult.ACTIVE,
            f"Missing controls: {', '.join(missing)}. DPDPA S.8(4) requires "
            f"'appropriate' self-assessed measures — gaps may be acceptable under "
            f"DPDPA if risk-assessed. 152-FZ Art.19 prescribes specific mandatory "
            f"controls per UZ classification level — these gaps constitute verifiable "
            f"FSTEC audit failures regardless of risk assessment.",
            "Implement layered compliance architecture: use 152-FZ UZ classification "
            "as the control floor. Enable all required controls per FSTEC Order No.21 "
            "for the applicable UZ level. DPDPA compliance follows automatically.",
        ))
    else:
        conflicts.append(ConflictResult(
            "CONFLICT-04",
            "DPDP-S8(4) vs 152FZ-A19(1)/A19(3)",
            ConflictResult.INACTIVE,
            "No observable security control prescription gaps detected in this "
            "configuration. Note: full CONFLICT-04 assessment requires FSTEC audit "
            "of all PDIS controls, not just storage configuration.",
            None,
        ))

    # ── CONFLICT-05: Transfer adequacy ────────────────────────────────────
    # If bucket is not in Russia but serves Russian users, transfers are occurring
    # that require 152-FZ Art.12(4) legal basis
    if origin in ("russian", "both") and not is_russian_region:
        conflicts.append(ConflictResult(
            "CONFLICT-05",
            "DPDP-S16 vs 152FZ-A12(1)",
            ConflictResult.ACTIVE,
            f"Data of Russian subjects stored in '{region}' (non-Russian territory). "
            f"DPDPA S.16 permits this transfer (no blacklist notification). 152-FZ "
            f"Art.12(1) requires a specific legal basis (Art.12(4)) since India is not "
            f"on Roskomnadzor's adequate countries list. Pre-transfer adequacy "
            f"verification must be documented per Art.12(3).",
            "Document the Art.12(4) legal basis for this transfer (written subject "
            "consent for transborder transfer, or contract performance basis). Build "
            "pre-transfer adequacy verification log. Monitor Roskomnadzor adequacy list "
            "for changes. Note: 242-FZ localization requirement takes precedence.",
        ))
    else:
        conflicts.append(ConflictResult(
            "CONFLICT-05",
            "DPDP-S16 vs 152FZ-A12(1)",
            ConflictResult.INACTIVE,
            f"Transfer adequacy conflict not triggered. "
            f"{'Data is in Russian territory — primary storage obligation satisfied.' if is_russian_region else 'No Russian data subjects in scope.'}",
            None,
        ))

    # ── CONFLICT-06: Automated Decision-Making ────────────────────────────
    # Not directly assessable from bucket config; flag as context-dependent
    if origin in ("russian", "both"):
        conflicts.append(ConflictResult(
            "CONFLICT-06",
            "DPDPA (absent) vs 152FZ-A16",
            ConflictResult.ACTIVE,
            "This storage serves Russian data subjects. If data in this bucket feeds "
            "any automated decision system producing legal consequences (credit scoring, "
            "fraud detection, eligibility assessment), 152-FZ Art.16 requires written "
            "consent for automated processing, an explanation engine, and a 30-day human "
            "objection review workflow. DPDPA has no equivalent provision.",
            "Audit all downstream systems consuming data from this bucket. For any "
            "system producing automated decisions with legal consequences for Russian "
            "subjects: (1) obtain written ADM consent, (2) implement explanation engine, "
            "(3) build 30-day objection handler. Apply 152-FZ standard globally for "
            "architectural simplicity and DPDPA future-proofing.",
        ))
    else:
        conflicts.append(ConflictResult(
            "CONFLICT-06",
            "DPDPA (absent) vs 152FZ-A16",
            ConflictResult.INACTIVE,
            "No Russian data subjects in scope — 152-FZ Art.16 ADM prohibition "
            "not applicable to this configuration.",
            None,
        ))

    return conflicts


